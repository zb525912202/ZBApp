        public int Compare(string one, string another)
        {
            if (one == null) { one = string.Empty; }
            if (another == null) { another = string.Empty; }
            if (one.Length > 0 && another.Length > 0)
            {
                char o = Letter(one[0]);
                char a = Letter(another[0]);
                return o - a;
            }
            else if (one.Length > 0 || another.Length > 0)
            {
                return one.Length - another.Length;
            }
            return 0;
        }

